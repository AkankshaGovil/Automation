#define MAX_LINE_SIZE 200

int
main(int argc, char **argv)
	{
	FILE *fp;
	int rv;
	unsigned char msg[2*MAX_LINE_SIZE] = {0};
	unsigned char *psigret,*p;
	int siglen;	
	int msglen ;
	int	i;

	
        fp = fopen(argv[1],"r+");
        if (!fp)
        {
                perror("Opening file ");
                exit(1);
        }

	for(i = 0,p = msg; i<2;++i)
	{
		fscanf(fp,"%[^'\n']",p);
		p+=strlen(p);
		fscanf(fp,"%c",p);
		p+=strlen(p);
	}
	fflush(fp);
	msglen = strlen(msg);
	lm_sign(msg,msglen,&psigret,&siglen);
	
	fprintf(fp,"%d ",siglen);
	fwrite(psigret,siglen,1,fp);
	fprintf(fp,"\n");
	fclose(fp);

	return(0);	
}


